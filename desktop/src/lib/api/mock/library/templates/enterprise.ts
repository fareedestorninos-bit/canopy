import type { CanopyAgent } from "$api/types";

const now = new Date().toISOString();
const zero = { input: 0, output: 0, cache_read: 0, cache_write: 0 } as const;

function agent(
  id: string,
  name: string,
  emoji: string,
  role: string,
  division: string,
  skills: string[],
  reportsTo: string | null,
  color: string,
  systemPrompt: string,
): CanopyAgent {
  return {
    id: `tpl-${id}`,
    name: id,
    display_name: name,
    avatar_emoji: emoji,
    role,
    status: "idle",
    adapter: "claude-code",
    model: "claude-sonnet-4-20250514",
    system_prompt: systemPrompt,
    config: {
      division,
      reportsTo,
      color,
      budget: 500,
      title: name,
      signal: "",
    },
    skills,
    schedule_id: null,
    budget_policy_id: null,
    current_task: null,
    last_active_at: now,
    token_usage_today: { ...zero },
    cost_today_cents: 0,
    created_at: now,
    updated_at: now,
  };
}

